public class Point {
    //attribute private x and y
    private double x;
    private double y;

    //constructor for Point
    public Point(double x, double y){
        this.x = x;
        this.y = y;
    }

    //getter method for x
    public double getX(){
        return x;
    }

    //getter method for y
    public double getY(){
        return y;
    }
}
